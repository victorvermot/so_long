char	*ft_strrchr(const char *s, int c)
{
	int	i;

	c = (char)c;
	i = -1;
	while (s[++i])
		;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
